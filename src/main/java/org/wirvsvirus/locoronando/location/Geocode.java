package org.wirvsvirus.locoronando.location;

public class Geocode {
	private Double lat;
	private Double lng;

	public Double getLng() {
		return this.lng;
	}

	public void setLng(Double lng) {
		this.lng = lng;
	}

	public Double getLat() {
		return this.lat;
	}

	public void setLat(Double lat) {
		this.lat = lat;
	}
}
